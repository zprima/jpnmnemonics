function findOriginalKana(en) {
  return kana.find((item) => item.en === en);
}

function nextRandomKana() {
  const random = Math.floor(Math.random() * kana.length);
  const newKana = kana[random];

  const originalKana = newKana.original_kana_eng != "" ? findOriginalKana(newKana.original_kana_eng) : newKana;

  const kdHiragana = document.querySelector('.kd_hiragana');
  const kdKatakana = document.querySelector('.kd_katakana');

  const kiHiragana = document.querySelector('.ki_hiragana');
  const kiKatakana = document.querySelector('.ki_katakana');

  const kiEn = document.querySelector('.ki_en');

  kdHiragana.innerHTML = newKana.hiragana;
  kdKatakana.innerHTML = newKana.katakana;

  kiHiragana.innerText = newKana.hiragana;
  kiKatakana.innerText = newKana.katakana;

  kiEn.innerText = newKana.en;

  document.getElementById("img_h").src = `/example1/mnemonics/h_${originalKana.en}.png`;
  document.getElementById("img_k").src = `/example1/mnemonics/k_${originalKana.en}.png`;


}